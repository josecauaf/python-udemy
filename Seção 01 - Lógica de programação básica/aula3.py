'''
Python = Linguagem de programação
Tipo de linguagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''
print(1234)

# Aspas simples
print('José Cauã')
print(1, 'José "Cauã"') # Versão limpa do "Escape"

# Aspas duplas
print("José Cauã")
print(2, "José 'Cauã'") # Versão limpa do "Escape"


# Escape (Ignora o próximo caractere após \)
print('José \"Cauã\"')

# r (Exibe o caractere de escape)
print(r'José \"Cauã\"')
