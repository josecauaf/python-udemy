# Variáveis são usadas para salvar algo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar número e uderline _.
# O sinal de - é o operador de atribuição. Ele é usado para atribuir um valor a um nome (variável).
# Uso: nome_variavel = expressão
nome_completo = 'José Cauã F. da Costa'
print(nome_completo) # => José Cauã F. da Costa
soma_dois_mais_dois = 2 + 2
print(soma_dois_mais_dois) # => 4

nome = 'José'
idade = 20
maior_de_idade = idade >= 18 # Operador '>= maior ou igual a'
print('Nome:', nome, 'Idade:', idade)
print('É maior?', maior_de_idade)